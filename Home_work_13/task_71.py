# Создайте функцию, которая запрашивает числовые данные от пользователя до тех пор,
# пока он не введёт целое или вещественное число.
# Обрабатывайте не числовые данные как исключения.

def get_numeric_input():
    while True:
        user_input = input("Введите число: ")

        try:
            # Пробуем преобразовать введенные данные в число (целое или вещественное)
            number = float(user_input)
            if number.is_integer():
                number = int(number)
            return number
        except ValueError:
            # Если не удалось преобразовать в число, сообщаем пользователю об ошибке
            print("Ошибка! Введите целое или вещественное число.")


# Пример использования функции
numeric_data = get_numeric_input()
print(f"Вы ввели число: {numeric_data}")
