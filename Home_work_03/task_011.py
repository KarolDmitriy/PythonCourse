# Три друга взяли вещи в поход. Сформируйте словарь, где ключ — имя друга, а значение — кортеж вещей. Напишите ответы на вопросы:
# - Какие вещи взяли все три друга.
# - Какие вещи уникальны, есть только у одного друга.
# - Какие вещи есть у всех друзей кроме одного и имя того, у кого данная вещь отсутствует
# Для решения используйте операции с множествами. Код должен расширяться на любое большее количество друзей.

friends_items = {
    'Друг 1': ('Рюкзак', 'Палатка', 'Спальный мешок', 'Фонарик'),
    'Друг 2': ('Рюкзак', 'Палатка', 'Котелок', 'Термос'),
    'Друг 3': ('Рюкзак', 'Палатка', 'Спальный мешок', 'Фонарик', 'Котелок'),
    'Друг 4': ('Рюкзак', 'Фонарик', 'Спички')
}

# Вещи, которые взяли все три друга
common_items = set.intersection(*[set(items) for items in friends_items.values()])
print("Вещи, которые взяли все три друга:", common_items)

# Вещи, которые есть только у одного друга
unique_items = set.union(*[set(items) for items in friends_items.values()]) - common_items
print("Вещи, которые есть только у одного друга:", unique_items)

# Вещи, которые есть у всех друзей кроме одного, и имя этого друга
for friend, items in friends_items.items():
    other_friends_items = set.union(*[set(friends_items[f]) for f in friends_items if f != friend])
    missing_items = other_friends_items - set(items)
    if missing_items:
        print("Вещи, которые есть у всех друзей кроме", friend + ":", missing_items)
